
public class Operacao {

	public float resultado;
	
	public float somar(float a, float b) {
		return (a + b);
	}

	public float subtracao(float a, float b) {
		return (a - b);
	}

	public float divisao(float a, float b) {
		return (a / b);
	}

	public float multiplicacao(float a, float b) {
		return (a * b);
	}
}
